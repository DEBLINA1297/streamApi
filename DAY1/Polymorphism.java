package DAY1;

public class Polymorphism
{
static void sample(int a)
 {
	 System.out.println(a);
 }
 static void sample(double b)
 {
	 System.out.println(b); 
 }
 static void sample(String c)
 {
	 System.out.println(c);  
 }
public static void main(String[] args)
{
sample(10);
sample(22.22);
sample("java");
}	
	
	
	
}
